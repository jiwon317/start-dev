package hello_world;

public class PrimitiveDataTypeExam {

	public static void main(String[] args) {
		boolean isFun = false;
		char charValue = 'a';
		int intValue = 20;
		long longValue = 2147483648L;
		
		System.out.println(isFun);
		System.out.println(charValue);
		System.out.println(intValue);
		System.out.println(longValue);
		
//		변수는 형, 변수명, 값 순서로 선언한다.
		
		
	}
//	변수를 선언할 때는 항상 자료형, ,변수명 순서로 선언해야 한다.
//	문자형에 값을 넣을 때는 반드시 작은 따옴표(',') 사용.
//	long형은 알파벳 L을 붙여야됨. 대소문자 구분 x
//	float형은 알파벳 F를 붙여야됨. 대소문자 구분 x
	
	
}