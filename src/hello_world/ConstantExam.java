package hello_world;
// 상수 : 저장한 데이터 공간이 바뀌지 않는 메모리 공간
public class ConstantExam {

	public static void main(String[] args) {
		final double PI = 3.14159;
		int radius = 5;
		double result = radius * 2 * PI;
		System.out.println(result);
		
		
		
		
				
//		상수는 대문자 사용이 관례, 1. 상수를 쓰면 실수를 줄일수 있다, 2. 값이 무엇을 의미하는지 알기 쉽다.
//		3. 값을 한번에 바꿀 수 있다.
//	 	대문자로만 구성된 명사로, 단어와 단어 사이는 '_"로 구분한다. 
//		final 형 이름;
//		final int J;
		
	
	}
}
